import math
angle_in_radians=float(input("Enter angle in radians:"))
result_sin=math.sin(angle_in_radians)
result_cos=math.cos(angle_in_radians)
result_tan=math.tan(angle_in_radians)
print("The sine value is:",result_sin)
print("The cosine value is:",result_cos)
print("The tangent value is:",result_tan)